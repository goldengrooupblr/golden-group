// Renders schema.org structured data. JSON.stringify output is escaped so
// closing-tag sequences in data can't break out of the script element.
export function JsonLd({ data }: { data: object }) {
  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{
        __html: JSON.stringify(data).replace(/</g, "\\u003c"),
      }}
    />
  );
}
